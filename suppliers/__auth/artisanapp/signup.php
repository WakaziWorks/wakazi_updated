                var email = $(this).val();
                $.ajax({
                    url: 'check_email.php',
                    method: 'POST',
                    data: {
                        email: email
                    },
                    success: function(response) {
                        if (response == 'taken') {
                            alert('Email already registered.');
                            $("#form3Example3c").val(''); // Clear the input field.
                        }
                    }
                });
            });
        });
